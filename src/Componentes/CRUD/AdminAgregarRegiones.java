    public void cargarRegiones() {
        modelo.setRowCount(0);
        totalRegiones = (ArrayList<Regiones>) ctrlRegiones.getTodasRegiones().clone();
        for (Regiones Region : totalRegiones) {
            Object datos[] = new Object[4];
            datos[0] = Region.getCodigo();
            datos[1] = Region.getNombre();
            datos[2] = Region.getPrecioEstandar();
            datos[3] = Region.getPrecioEspecial();
            modelo.addRow(datos);
        }
    }
    
    public void agregarRegion(){
        if (ctrlRegiones.nuevaRegion(txtCodigo.getText().toString(), txtNombreR.getText().toString(), Double.parseDouble(txtPrecioEst.getText()),  Double.parseDouble(txtPrecioEspe.getText()), 0)) {
            JOptionPane.showMessageDialog(null, "Region ingresada con exito");
            cargarRegiones();
        }
    }

